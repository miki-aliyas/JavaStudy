package bronze.purple.chapter4;

public class Sample4_1 {
    public static void main(String[] args) {
        int num = 0;

        while (num < 5) {
//            numの値が5未満の間以下を繰り返す
            System.out.println("numの値は" + num + "です。");
//            numの値に1を加算
            num++;
        }
    }
}
